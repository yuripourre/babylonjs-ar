/**
 * Get dictionary by size
 */
export function getArucoDictionary(size: 4 | 5 | 6): number[][] {
  switch (size) {
    case 4:
      return ARUCO_4X4_50;
    case 5:
      return ARUCO_5X5_100;
    case 6:
      return ARUCO_6X6_250;
    default:
      return ARUCO_4X4_50;
  }
}

/**
 * Get dictionary size (number of markers)
 */
export function getDictionarySize(size: 4 | 5 | 6): number {
  switch (size) {
    case 4:
      return 50;
    case 5:
      return 100;
    case 6:
      return 250;
    default:
      return 50;
  }
}

/**
 * Validate marker pattern dimensions
 */
export function validateMarkerPattern(pattern: number[], size: 4 | 5 | 6): boolean {
  const expectedLength = size * size;
  if (pattern.length !== expectedLength) {
    return false;
  }

  // All values should be 0 or 1
  return pattern.every(bit => bit === 0 || bit === 1);
}
